export const useActiveNwsAlertsByType = (eventTypeString) => {
  return useQuery({
    queryKey: ["NWS API Web Service", "Alerts", "Active", eventTypeString],
    queryFn: () => fetchActiveNwsAlertsByType(eventTypeString),
    refetchInterval: 15000,
  });
};
